                            {
                                label: "禁用",
                                value: 0,
                                type: "danger"
                            }
                        ]
                    },
                    {
                        prop: "createTime",
                        label: "创建时间",
                        align: "center",
                        sortable: "custom",
                        "min-width": 150
                    },
                    {
                        align: "center",
                        type: "op",
                        layout: ["slot-move-btn", "edit", "delete"],
