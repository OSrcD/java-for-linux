                        <el-row>
                            <io-table
                                ref="table"
                                :columns="table.columns"
                                :props="table.props"
                                @selection-change="onSelectionChange"
                            >
                                <!-- 头像 -->
                                <template #column-headImg="{ scope }">
                                    <io-avatar
                                        shape="square"
                                        size="medium"
                                        :src="scope.row.headImg"
                                        :style="{ margin: 'auto' }"
                                    >
                                    </io-avatar>
                                </template>

                                <!-- 权限 -->
                                <template #column-roleName="{ scope }">
                                    <el-tag
                                        v-for="(item, index) in parseRole(scope.row.roleName)"
                                        :key="index"
                                        size="small"
                                        effect="dark"
                                        style="margin: 2px"
                                    >{{ item }}</el-tag
                                    >
                                </template>

                                <!-- 单个转移 -->
                                <template #slot-move-btn="{ scope }">
                                    <el-button
                                        type="text"
                                        size="mini"
                                        @click="toMove(scope.row)"
                                    >转移</el-button
                                    >
                                </template>
                            </io-table>
                        </el-row>

                        <el-row type="flex">
                            <io-pagination></io-pagination>
                        </el-row>

                        <io-upsert
                            ref="upsert"
                            :items="upsert.items"
                            :on-submit="onUpsertSubmit"
                            @open="onUpsertOpen"
                        ></io-upsert>
                    </io-crud>
                </div>
            </div>
        </div>

        <!-- 部门移动 -->
        <io-dept-move ref="dept-move" @success="refresh({ page: 1 })"></io-dept-move>
    </div>
</template>

<script>
import { isPc } from "@/iooc/utils";

export default {
    data() {
        return {
            isExpand: isPc(),
            selects: {
                dept: {},
                ids: []
            },
            dept: [],
            table: {
                props: {
                    "default-sort": {
                        prop: "createTime",
