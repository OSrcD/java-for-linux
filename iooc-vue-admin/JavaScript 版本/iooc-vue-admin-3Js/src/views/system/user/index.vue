                        "min-width": 150
                    },
                    {
                        prop: "nickName",
                        label: "昵称",
                        align: "center",
                        "min-width": 150
                    },
                    {
                        prop: "departmentName",
                        label: "部门名称",
                        align: "center",
                        "min-width": 150
                    },
                    {
                        prop: "roleName",
                        label: "角色",
